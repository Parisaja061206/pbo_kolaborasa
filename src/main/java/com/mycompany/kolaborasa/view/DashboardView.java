    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
    int barisTerpilih = jTable1.getSelectedRow();
    
    // Validasi: Pastikan user sudah mengklik salah satu baris di tabel
    if (barisTerpilih == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih baris data di tabel yang ingin di-update terlebih dahulu!");
        return;
    }
    
    // 1. Ambil ID dari kolom ke-0 pada baris yang dipilih
    String id = jTable1.getValueAt(barisTerpilih, 0).toString();
    String judulLama = jTable1.getValueAt(barisTerpilih, 1).toString();
    
    // 2. Munculkan pop-up input untuk mengisi Judul Baru
    String judulBaru = javax.swing.JOptionPane.showInputDialog(this, "Masukkan Judul Baru:", judulLama);
    if (judulBaru == null || judulBaru.trim().isEmpty()) return; // Batal jika kosong/cancel
    
    // 3. Munculkan pop-up input untuk mengisi Deskripsi Baru
    String deskripsiBaru = javax.swing.JOptionPane.showInputDialog(this, "Masukkan Deskripsi Baru:");
    if (deskripsiBaru == null || deskripsiBaru.trim().isEmpty()) return; // Batal jika kosong/cancel
    
    // 4. Kirim ke controller untuk di-update ke database
    controller.updateIde(id, judulBaru, deskripsiBaru);
    
    // 5. Refresh tabel dashboard
    controller.loadDataDashboard(jTable1);                      
    
    }//GEN-LAST:event_btnUpdateActionPerformed

