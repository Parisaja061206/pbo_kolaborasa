    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
                                         
    int barisTerpilih = jTable1.getSelectedRow();
    
    // Validasi: Pastikan user sudah mengklik salah satu baris di tabel
    if (barisTerpilih == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Pilih baris data di tabel yang ingin dihapus terlebih dahulu!");
        return;
    }
    
    // 1. Ambil ID dari kolom ke-0 pada baris yang dipilih
    String id = jTable1.getValueAt(barisTerpilih, 0).toString();
    String judul = jTable1.getValueAt(barisTerpilih, 1).toString();
    
    // 2. Tampilkan pop-up konfirmasi hapus
    int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(DashboardView.this, 
            "Apakah kamu yakin ingin menghapus ide '" + judul + "'?", "Konfirmasi Hapus", 
            javax.swing.JOptionPane.YES_NO_OPTION);
            
    if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
        // 3. Eksekusi hapus via controller
        controller.deleteIde(id);
        
        // 4. Refresh tabel dashboard
        controller.loadDataDashboard(jTable1);
    }

// TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

